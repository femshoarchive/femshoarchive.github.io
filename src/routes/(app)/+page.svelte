<h2>Who the hell is FemSho?</h2>
FemSho is a character originally drawn by <a href="https://twitter.com/pompmaker1">Pompmaker1</a> based on <a href="https://www.youtube.com/channel/UCHlTEt24Yb4ylJFuWz7hXIw">Shounic's</a> (<a href="/andyherdscomics.webp">not</a>) original character, Shocat but drawn as a femboy. Why is this such a big deal? Like a month later since that artwork was drawn, <a href="https://www.youtube.com/channel/UCUnDDhXzfasuvzEZfO09nYw">Delta Time</a> and other people persuaded Shounic into using FemShoe as his PNGTuber avatar with the power of $700.

